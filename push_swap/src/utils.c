#include "push_swap.h"

void	ft_putnbr(int n)
{
	if (n == -2147483648)
		ft_putstr_fd("-2147483648", 1);
	else
	{
		if (n < 0)
		{
			ft_putchar_fd('-', 1);
			n = -n;
		}
		if (n >= 10)
		{
			ft_putnbr(n / 10);
			ft_putnbr(n % 10);
		}
		else
		{
			ft_putchar_fd(n + '0', 1);
		}
	}
}
